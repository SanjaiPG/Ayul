  static const Color primaryVariantLight = Color(0xFF1F3A0F); // Darker sage
  static const Color secondaryLight = Color(0xFF8B4513); // Warm earth brown
  static const Color secondaryVariantLight = Color(0xFF6B3410); // Darker brown
  static const Color accentLight = Color(0xFFFF6B35); // Vibrant saffron
  static const Color backgroundLight = Color(0xFFFAFAF9); // Warm off-white
  static const Color surfaceLight = Color(0xFFFFFFFF); // Pure white
  static const Color errorLight = Color(0xFFFF6B35); // Using accent for errors
  static const Color successLight = Color(0xFF4CAF50); // Standard green
  static const Color warningLight = Color(0xFFFFC107); // Amber
  static const Color onPrimaryLight = Color(0xFFFFFFFF);
  static const Color onSecondaryLight = Color(0xFFFFFFFF);
  static const Color onBackgroundLight = Color(0xFF1A1A1A); // Near-black
  static const Color onSurfaceLight = Color(0xFF1A1A1A); // Near-black
  static const Color onErrorLight = Color(0xFFFFFFFF);

  // Dark theme adaptations maintaining medical authority
  static const Color primaryDark = Color(0xFF4A7A2A); // Lighter sage for dark
  static const Color primaryVariantDark = Color(0xFF2D5016); // Original sage
