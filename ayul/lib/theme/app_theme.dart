  static const Color secondaryVariantDark = Color(0xFF8B4513); // Original brown
  static const Color accentDark = Color(0xFFFF8A5B); // Softer saffron
  static const Color backgroundDark = Color(0xFF121212); // Material dark
  static const Color surfaceDark = Color(0xFF1E1E1E); // Elevated surface
  static const Color errorDark = Color(0xFFFF8A5B); // Softer accent
  static const Color successDark = Color(0xFF66BB6A); // Lighter green
  static const Color warningDark = Color(0xFFFFD54F); // Lighter amber
  static const Color onPrimaryDark = Color(0xFFFFFFFF);
  static const Color onSecondaryDark = Color(0xFFFFFFFF);
  static const Color onBackgroundDark = Color(0xFFFFFFFF);
  static const Color onSurfaceDark = Color(0xFFFFFFFF);
  static const Color onErrorDark = Color(0xFF000000);

  // Semantic colors for medical content
  static const Color textPrimaryLight = Color(0xFF1A1A1A); // Near-black
  static const Color textSecondaryLight = Color(0xFF666666); // Medium gray
  static const Color borderSubtleLight = Color(0xFFE8E8E8); // Minimal border

  static const Color textPrimaryDark = Color(0xFFFFFFFF);
  static const Color textSecondaryDark = Color(
    0xFFB3B3B3,
